//trigger animations
$('body').on('click', '.giffer', function() {
  
    var state = $(this).attr('data-state')

    console.log(state)

    if (state == 'still') {
        $(this).attr('src', $(this).attr('data-anim'));
        $(this).attr('data-state', 'anim');

    }
    else if (state == 'anim') {
        $(this).attr('src', $(this).attr('data-still'));
        $(this).attr('data-state', 'still');
    }
})

